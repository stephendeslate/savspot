/**
 * Prebuild script: reads docs/help-center/ markdown files and generates
 * a TypeScript module at src/generated/helpContent.ts for the in-app help center.
 *
 * Run: node scripts/generate-help-content.mjs
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const HELP_DIR = path.resolve(__dirname, '../../../docs/help-center');
const OUT_FILE = path.resolve(__dirname, '../src/generated/helpContent.ts');

function extractTitle(markdown) {
  const match = markdown.match(/^#\s+(.+)$/m);
  return match ? match[1].trim() : 'Untitled';
}

function extractDescription(markdown) {
  const lines = markdown.split('\n');
  let pastTitle = false;
  for (const line of lines) {
    const trimmed = line.trim();
    if (!pastTitle) {
      if (trimmed.startsWith('# ')) pastTitle = true;
      continue;
    }
    if (trimmed === '' || trimmed === '---' || trimmed.startsWith('#')) continue;
    return trimmed;
  }
  return '';
}

function stripMarkdown(markdown) {
  return markdown
    .replace(/^#{1,6}\s+/gm, '')
    .replace(/\[([^\]]+)\]\([^)]+\)/g, '$1')
    .replace(/\*{1,3}([^*]+)\*{1,3}/g, '$1')
    .replace(/`([^`]+)`/g, '$1')
    .replace(/!\[[^\]]*\]\([^)]+\)/g, '')
    .replace(/^---+$/gm, '')
    .replace(/^[\s]*[-*+]\s+/gm, '')
    .replace(/\n{2,}/g, '\n')
    .trim();
}

function main() {
  if (!fs.existsSync(HELP_DIR)) {
    console.log('No help-center directory found, generating empty helpContent.ts');
    const emptyOutput = `// Auto-generated by scripts/generate-help-content.mjs — do not edit manually

export interface HelpCollection {
  id: string;
  title: string;
  description: string;
  articleCount: number;
}

export interface HelpArticle {
  slug: string;
  collectionId: string;
  title: string;
  body: string;
}

export interface SearchEntry {
  slug: string;
  collectionId: string;
  title: string;
  plaintext: string;
}

export const collections: HelpCollection[] = [];

export const articles: HelpArticle[] = [];

export const searchIndex: SearchEntry[] = [];
`;
    fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
    fs.writeFileSync(OUT_FILE, emptyOutput, 'utf-8');
    return;
  }

  const collections = [];
  const articles = [];
  const searchIndex = [];

  const entries = fs.readdirSync(HELP_DIR, { withFileTypes: true });
  const collectionDirs = entries
    .filter((e) => e.isDirectory())
    .sort((a, b) => a.name.localeCompare(b.name));

  for (const dir of collectionDirs) {
    const collectionId = dir.name;
    const collectionPath = path.join(HELP_DIR, collectionId);
    const indexPath = path.join(collectionPath, 'index.md');

    let collectionTitle = collectionId.replace(/-/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase());
    let collectionDescription = '';
    if (fs.existsSync(indexPath)) {
      const indexContent = fs.readFileSync(indexPath, 'utf-8');
      collectionTitle = extractTitle(indexContent);
      collectionDescription = extractDescription(indexContent);
    }

    const mdFiles = fs
      .readdirSync(collectionPath)
      .filter((f) => f.endsWith('.md') && f !== 'index.md')
      .sort();

    collections.push({
      id: collectionId,
      title: collectionTitle,
      description: collectionDescription,
      articleCount: mdFiles.length,
    });

    for (const file of mdFiles) {
      const slug = file.replace(/\.md$/, '');
      const body = fs.readFileSync(path.join(collectionPath, file), 'utf-8');
      const title = extractTitle(body);

      articles.push({ slug, collectionId, title, body });
      searchIndex.push({
        slug,
        collectionId,
        title,
        plaintext: stripMarkdown(body),
      });
    }
  }

  const output = `// Auto-generated by scripts/generate-help-content.mjs — do not edit manually

export interface HelpCollection {
  id: string;
  title: string;
  description: string;
  articleCount: number;
}

export interface HelpArticle {
  slug: string;
  collectionId: string;
  title: string;
  body: string;
}

export interface SearchEntry {
  slug: string;
  collectionId: string;
  title: string;
  plaintext: string;
}

export const collections: HelpCollection[] = ${JSON.stringify(collections, null, 2)};

export const articles: HelpArticle[] = ${JSON.stringify(articles, null, 2)};

export const searchIndex: SearchEntry[] = ${JSON.stringify(searchIndex, null, 2)};
`;

  fs.mkdirSync(path.dirname(OUT_FILE), { recursive: true });
  fs.writeFileSync(OUT_FILE, output, 'utf-8');

  console.log(
    `Generated helpContent.ts: ${collections.length} collections, ${articles.length} articles`,
  );
}

main();
